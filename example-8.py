#using if else to find the number of week days
week = eval(input("Enter a value: "))
if week==1:
    print("It's Sunday!")
elif week==2:
    print("It's Monday!")
elif week==3:
    print("It's Tuesday!")
elif week==4:
    print("It's Wednesday!")
elif week==5:
    print("It's Thursday!")
elif week==6:
    print("It's Friday!")
elif week==7:
    print("It's Saturday!")
else: 
    print("Invalid Input!")